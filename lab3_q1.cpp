//Include the library
#include<iostream>
using namespace std;
//Create main
int main()
{
	//Declaration of variables of all types
	int i;
	float j;
	char a;
	bool n;
	//Assigning value to the variables
	i=10;
	j=12.5;
	a='K';
	n=1;
	//Printing values assigned to the variables
	cout<<"Values assigned in the data types: ";
	cout<<i<<" , ";
	cout<<j<<" , ";
	cout<<a<<" , ";
	cout<<n<<" , ";
	//Printing size of the variables
	cout<<"Size of the data types are :";
	cout<<sizeof(i)<<" , ";
	cout<<sizeof(j)<<" , ";
	cout<<sizeof(a)<<" , ";
	cout<<sizeof(n);
	return 0;
}
	
